package assignment;

public class Stringreverse {

	public static void main(String[] args) {
		String n="Amritha";
		String rev=" ";
		for(int i=n.length()-1;i>=0;i--)
		{
			rev=rev+n.charAt(i);
		}
		System.out.println("String :"+n);
		System.out.println("String reverse:"+rev)                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                      ;

	}

}
