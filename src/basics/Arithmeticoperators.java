package basics;

public class Arithmeticoperators {

	public static void main(String[] args) {
		
		int a=60;
		int b=20;
		int c=a+b;
		System.out.println("addition:"+c);
		
		int d=30;
		int e=10;
		int f=d-e;
		System.out.println("subtraction:"+f);
		
		int p=40;
		int q=20;
		int r=p*q;
		System.out.println("mutiplication:"+r);
		
		int m=90;
		int n=30;
		int o=m/n;
		System.out.println("division"+o);
		
		int x=20;
		int y=10;
		int z=x%y;
		System.out.println("modulus:"+z);
	

	}

}
