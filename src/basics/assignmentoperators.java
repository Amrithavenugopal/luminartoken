package basics;

public class assignmentoperators {

	public static void main(String[] args) {
		
		int a=55;
		System.out.println(a);
		int b;
		b=a;
		System.out.println(b);
		b+=a;
		System.out.println(b);
		b-=a;
		System.out.println(b);
		b*=a;
		System.out.println(b);
		b/=a;
		System.out.println(b);
	

	}

}
